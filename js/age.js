let age = 17

if (age <= 2) {
    console.log("infant")
} else if (age === 3) {
    console.log("toddler")
} else if (age>=4 && age<=7) {
    console.log("child")
} else if (age>=8 && age<=12) {
    console.log("preteen")
} else if (age>=13 && age<=17) {
    console.log("teen")
} else if (age>=18 && age<=20) {
    console.log("young adult")
}

